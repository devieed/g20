/** 根据设置推断实际使用的语言代码 */
export function getEffectiveLang(langSetting) {
  if (langSetting === 'auto') {
    const bl = (navigator.language || 'en').split('-')[0].toUpperCase()
    return SUPPORTED_LANGS.includes(bl) ? bl : 'EN'
  }
  return SUPPORTED_LANGS.includes(langSetting) ? langSetting : 'EN'
}

/** Vue composable：提供 t() 翻译函数和 locale 信息 */
export function useI18n() {
  const store = useTestersStore()

  const effectiveLang = computed(() => getEffectiveLang(store.settings.language))
  const locale = computed(() => LANG_LOCALE[effectiveLang.value] || 'en-US')
  const isRTL = computed(() => effectiveLang.value === 'AR')

  function t(key, params = {}) {
    const lang = effectiveLang.value
    const dict = translations[lang] || translations['EN']
    let str = dict[key] !== undefined ? dict[key] : (translations['EN'][key] || key)
    Object.entries(params).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v)
    })
    return str
  }

  return { t, effectiveLang, locale, isRTL }
}
